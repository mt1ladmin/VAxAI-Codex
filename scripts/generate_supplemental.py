def norm_name(name: str) -> str:
    n = (name or "").lower().strip()
    n = re.sub(r"[^a-z0-9]+", " ", n)
    for drop in ("ltd", "limited", "the", "charity", "cic", "llp"):
        n = re.sub(rf"\b{drop}\b", "", n)
    return re.sub(r"\s+", " ", n).strip()


