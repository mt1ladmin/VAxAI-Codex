def cc(charity_number: str) -> str:
    return (
        "https://register-of-charities.charitycommission.gov.uk/en/charity-search/"
        f"-/charity-details/{charity_number}/full-print"
    )


def p(
    organisation_name: str,
    location: str,
    region: str,
    website: str,
    *,
    organisation_type: str = "Charity",
    employees: int | None = None,
    annual_revenue_gbp: int | None = None,
    revenue_basis: str = "",
    need_score: int = 4,
    need_rationale: str = "",
    decision_maker_name: str = "",
    decision_maker_role: str = "Chief Executive",
    email: str = "",
    phone: str = "",
    financial_source_url: str = "",
    contact_source_url: str = "",
    data_confidence: str = "Medium",
    sector_tags: list[str] | None = None,
    pain_point_tags: list[str] | None = None,
    engagement_approach: str = "",
) -> dict:
    return record(
        organisation_name=organisation_name,
        organisation_type=organisation_type,
        location=location,
        region=region,
        website=website,
        employees=employees,
        annual_revenue_gbp=annual_revenue_gbp,
        revenue_basis=revenue_basis,
        need_score=need_score,
        need_rationale=need_rationale,
        decision_maker_name=decision_maker_name,
        decision_maker_role=decision_maker_role,
        email=email,
        phone=phone,
        financial_source_url=financial_source_url,
        contact_source_url=contact_source_url or website,
        data_confidence=data_confidence,
        sector_tags=sector_tags or [],
        pain_point_tags=pain_point_tags or [],
        engagement_approach=engagement_approach,
    )


