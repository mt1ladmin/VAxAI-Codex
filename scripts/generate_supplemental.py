def filter_excluded(prospects: list[dict]) -> list[dict]:
    kept: list[dict] = []
    seen: set[str] = set()
    for prospect in prospects:
        key = norm_name(prospect["organisation_name"])
        if not key or key in CSV_TOP_20_EXCLUDE or key in seen:
            continue
        seen.add(key)
        kept.append(prospect)
    return kept


